#!/bin/bash
# NEUXA 房源文案生成器 v1.0
# 使用方式: ./property-writer.sh "地址" "坪數" "房型" "樓層" "屋齡" "特色" "周邊" "價格"

ADDRESS="$1"
SIZE="$2"
ROOM="$3"
FLOOR="$4"
AGE="$5"
FEATURES="$6"
NEARBY="$7"
PRICE="$8"

if [ -z "$ADDRESS" ]; then
    echo "使用方法: ./property-writer.sh \"地址\" \"坪數\" \"房型\" \"樓層\" \"屋齡\" \"特色\" \"周邊\" \"價格\""
    echo "範例: ./property-writer.sh \"桃園市楊梅區中山路123號\" \"35\" \"3房2廳2衛\" \"5F/12F\" \"5\" \"近火車站、附車位\" \"楊梅站5分鐘、全聯2分鐘\" \"1288\""
    exit 1
fi

# 呼叫 Ollama 生成文案
ollama run qwen3:8b "你是一位專業的房地產文案撰寫專家，專精桃園楊梅區市場。

請根據以下房源資訊，生成一則專業的房源文案：

【房源資訊】
- 地址：${ADDRESS}
- 坪數：${SIZE} 坪
- 房型：${ROOM}
- 樓層：${FLOOR}
- 屋齡：${AGE} 年
- 特色：${FEATURES}
- 周邊：${NEARBY}
- 價格：${PRICE} 萬

【要求】
1. 標題 20 字以內，強調最大賣點
2. 文案分六區塊：開場、空間、特色（3點）、周邊、適合客群、行動呼籲
3. 語氣專業、誠實，不誇大
4. 禁止出現：保證、一定、絕對、無風險、穩賺等詞彙
5. 如有瑕疵或特殊狀況，必須主動提及並建議現場查看
6. 最後加入免責聲明：「⚠️ 房地產交易有風險，建議親自勘查並諮詢專業人士」

請用繁體中文生成。"

echo ""
echo "========================================"
echo "房源文案生成完成 | NEUXA v1.0"
echo "========================================"
